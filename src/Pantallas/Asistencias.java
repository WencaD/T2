    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        int indexEstado = cboEstado.getSelectedIndex();
        int index = cboEstudiante.getSelectedIndex();

        if (indexEstado >= 0){
            if (indexEstado == 0){
                curso.getEstudiantes().get(index).marcaAsistencia();
            }
            else if (indexEstado == 1){
                curso.getEstudiantes().get(index).marcaTardanza();
            }
            else{
                curso.getEstudiantes().get(index).marcaFalta();
           
        }
        JOptionPane.showMessageDialog(null, "Estudiante \n"
                        + "Codigo: " + curso.getEstudiantes().get(index).getCodigo() + "\n"
                        + "Nombre: " + curso.getEstudiantes().get(index).getNombre() + "\n"
                        + "Asitencias: " + curso.getEstudiantes().get(index).getAsistencia() + "\n"
                        + "Tardanzas: " + curso.getEstudiantes().get(index).getTardanza() + "\n"
                        + "Falta: " + curso.getEstudiantes().get(index).getFalta(),
                    "Asitencia", JOptionPane.INFORMATION_MESSAGE); 
                                           
    }//GEN-LAST:event_btnRegistrarActionPerformed
 }
